let add = (a, b) => {
    return a+b;
}

let subtract = (a, b) => {
    return a-b;
}

let multiply = (a, b) => {
    return a*b;
}

let divide = (a, b) => {
    return a/b;
}

let increment = (n) => {
    n++;
    return n;
}

let decrement = (n) => {
    n--
    return n;
}

let makeInt = (n) => {
    return parseInt(n, 10);
}

let preserveDecimal = (n) => {
    return parseFloat(n);
}